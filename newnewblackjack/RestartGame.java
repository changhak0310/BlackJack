package newnewblackjack;

import java.util.ArrayList;

public class RestartGame {

	public ArrayList<Card> reCard(ArrayList<Card> card) {
		card.clear();
		return card;
	}

	public int reBettingChip(int bettingChip) {
		bettingChip = 0;
		return bettingChip;
	}

	public int reSum(int sum) {
		sum = 0;
		return sum;
	}
}
